pso_options.w_max = eval(get(handles.edit19,'String'));
tmp=get(handles.popupmenu1,'String');
tmp_ind=get(handles.popupmenu1,'Value');
pso_options.method =tmp{tmp_ind};
rapidSettings.psoSettings = pso_options;
close(gcf)



