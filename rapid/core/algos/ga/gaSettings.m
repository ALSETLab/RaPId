% --- Executes just before gaSettings is made visible.
function gaSettings_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to gaSettings (see VARARGIN)

% Choose default command line output for gaSettings
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);
handle2main=getappdata(0,'HandleMainGUI');
RaPIdObject=getappdata(handle2main,'RaPIdObject');
ga_options=RaPIdObject.gaSettings;
try 
    set(handles.edit1,'String',ga_options.nbCromosomes);
    set(handles.edit2,'String',ga_options.nbCroossOver1);
    set(handles.edit3,'String',ga_options.nbCroossOver2);
    set(handles.edit4,'String',ga_options.nbMutations);
    set(handles.edit5,'String',ga_options.nbReproduction);
    set(handles.edit6,'String',ga_options.limit);
    set(handles.edit7,'String',ga_options.fitnessStopRatio);
    set(handles.edit8,'String',ga_options.headSize1);
    set(handles.edit9,'String',ga_options.headSize2);
    set(handles.edit10,'String',ga_options.headSize3);
    set(handles.edit11,'String',ga_options.nbReinjection);
    set(handles.edit13,'String',ga_options.nRandMin);
catch error
    disp(error.message);
end



