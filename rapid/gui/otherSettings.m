rapidObject.combiSettings.firstMethod = firstMethod{get(handles.popupmenu1,'value')};
secondMethod = get(handles.popupmenu2,'String');
rapidObject.combiSettings.secondMetod = secondMethod{get(handles.popupmenu2,'value')};
close(gcf)



