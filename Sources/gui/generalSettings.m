% --- Executes just before generalSettings is made visible.
function generalSettings_OpeningFcn(hObject, eventdata, handles, varargin)
% This function has no output args, see OutputFcn.
% hObject    handle to figure
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
% varargin   command line arguments to generalSettings (see VARARGIN)

% Choose default command line output for generalSettings
handles.output = hObject;

% Update handles structure
guidata(hObject, handles);

% UIWAIT makes generalSettings wait for user response (see UIRESUME)
% uiwait(handles.figure1);
handle2main=getappdata(0,'HandleMainGUI');
RaPIdObject=getappdata(handle2main,'RaPIdObject');
set(handles.edit1,'String',RaPIdObject.experimentSettings.ts);
set(handles.edit2,'String',RaPIdObject.experimentSettings.tf);
set(handles.timeout_value,'String',RaPIdObject.experimentSettings.timeOut);
set(handles.edit11,'String',RaPIdObject.experimentSettings.integrationMethod);
set(handles.togglebutton1,'Value',RaPIdObject.experimentSettings.saveHist);
set(handles.edit9,'String',num2str(RaPIdObject.experimentSettings.cost_type));
dummy=cellstr(['';'']); %make sure that we have no empty cell arrays
if ~isempty(RaPIdObject.fmuInputNames)
    tmp1=(RaPIdObject.fmuInputNames);
else
    tmp1=dummy;
end

tmp7=num2cell(RaPIdObject.experimentSettings.objective_weights);

maxAlloc=max([length(tmp1),length(RaPIdObject.fmuOutputNames),length(RaPIdObject.parameterNames)])+1; %allocate space for the longest vector and and an extra editable field
dataAlloc=cell(7,maxAlloc);
dataAlloc(1,1:length(tmp1))=tmp1;
dataAlloc(2,1:length(RaPIdObject.fmuOutputNames))=RaPIdObject.fmuOutputNames;
dataAlloc(3,1:length(RaPIdObject.parameterNames))=RaPIdObject.parameterNames;
dataAlloc(4,1:length(RaPIdObject.experimentSettings.p_min))=num2cell(RaPIdObject.experimentSettings.p_min);
dataAlloc(5,1:length(RaPIdObject.experimentSettings.p_max))=num2cell(RaPIdObject.experimentSettings.p_max);
dataAlloc(6,1:length(RaPIdObject.experimentSettings.p_0))=num2cell(RaPIdObject.experimentSettings.p_0);
dataAlloc(7,1:length(tmp7))=tmp7;
set(handles.InputNames,'Data',dataAlloc);
set(handles.InputNames,'ColumnEditable',true(ones(1,maxAlloc)));
tmp8=cell([1,maxAlloc]);
tmp8=cellfun(@(x){'char'},tmp8);
set(handles.InputNames,'ColumnFormat',tmp8);
if (get(handles.togglebutton1,'Value'))==1
    set(handles.togglebutton1,'String','Save History: On')
else
    set(handles.togglebutton1,'String','Save History: Off')
end
if RaPIdObject.experimentSettings.verbose==1
    tmp2=1;
else
    tmp2=0;
end

set(handles.verbose,'Value',tmp2);
if (get(handles.verbose,'Value'))==1
    set(handles.verbose,'String','Verbose: On')
else
    set(handles.verbose,'String','Verbose: Off')
end
try
    set(handles.edit16,'String',int2str(RaPIdObject.experimentSettings.maxIterations))
end

try
    set(handles.edit15,'String',num2str(RaPIdObject.experimentSettings.t_fitness_start));
end





